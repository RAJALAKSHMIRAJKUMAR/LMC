-- VERSION:0.2 --DATE:26/03/2015 --DESC:EMP_ID CHANGED AS ULD_ID IN TICKLER HISTORY,LMC_TEAM_EMPLOYEE_REPORT_DETAILS --DONE BY:RL

DROP TRIGGER IF EXISTS TRG_LMC_TEAM_EMPLOYEE_REPORT_DETAILS_UPDATE;
CREATE TRIGGER TRG_LMC_TEAM_EMPLOYEE_REPORT_DETAILS_UPDATE
AFTER UPDATE ON LMC_TEAM_EMPLOYEE_REPORT_DETAILS
FOR EACH ROW

BEGIN 

	DECLARE OLD_VALUE TEXT DEFAULT '';
	DECLARE NEW_VALUE TEXT DEFAULT '';

	IF((OLD.TRD_ID!= NEW.TRD_ID) OR (OLD.ULD_ID!= NEW.ULD_ID) OR
 	(OLD.TERD_START_TIME IS NULL AND NEW.TERD_START_TIME IS NOT NULL) OR
 	(OLD.TERD_START_TIME IS NOT NULL AND NEW.TERD_START_TIME IS NULL) OR
 	(OLD.TERD_START_TIME!=NEW.TERD_START_TIME) OR
 	(OLD.TERD_END_TIME IS NULL AND NEW.TERD_END_TIME IS NOT NULL) OR
 	(OLD.TERD_END_TIME IS NOT NULL AND NEW.TERD_END_TIME IS NULL) OR
 	(OLD.TERD_END_TIME!=NEW.TERD_END_TIME) OR
 	(OLD.TERD_OT IS NULL AND NEW.TERD_OT IS NOT NULL) OR
 	(OLD.TERD_OT IS NOT NULL AND NEW.TERD_OT IS NULL) OR
 	(OLD.TERD_OT!=NEW.TERD_OT) OR
 	(OLD.TERD_REMARK IS NULL AND NEW.TERD_REMARK IS NOT NULL) OR
 	(OLD.TERD_REMARK IS NOT NULL AND NEW.TERD_REMARK IS NULL) OR
 	(OLD.TERD_REMARK!=NEW.TERD_REMARK))THEN
		SET OLD_VALUE = CONCAT(OLD_VALUE,'TERD_ID=', OLD.TERD_ID,','); 
	END IF;
	
	IF (OLD.TRD_ID!= NEW.TRD_ID) THEN  
		SET OLD_VALUE = CONCAT(OLD_VALUE,'TRD_ID=', OLD.TRD_ID,','); 
		SET NEW_VALUE = CONCAT(NEW_VALUE,'TRD_ID=', NEW.TRD_ID,','); 
	END IF;

	IF (OLD.ULD_ID!= NEW.ULD_ID) THEN 
		SET OLD_VALUE = CONCAT(OLD_VALUE,'ULD_ID=', OLD.ULD_ID,','); 
		SET NEW_VALUE = CONCAT(NEW_VALUE,'ULD_ID=', NEW.ULD_ID,','); 
	END IF;

	IF (OLD.TERD_START_TIME IS NULL AND NEW.TERD_START_TIME IS NOT NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'TERD_START_TIME=','<NULL>,');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'TERD_START_TIME=',NEW.TERD_START_TIME,',');
	ELSEIF(OLD.TERD_START_TIME IS NOT NULL AND NEW.TERD_START_TIME IS NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'TERD_START_TIME=',OLD.TERD_START_TIME,',');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'TERD_START_TIME=','<NULL>,');
	ELSEIF (OLD.TERD_START_TIME!= NEW.TERD_START_TIME) THEN 
		SET OLD_VALUE = CONCAT(OLD_VALUE,'TERD_START_TIME=', OLD.TERD_START_TIME,','); 
		SET NEW_VALUE = CONCAT(NEW_VALUE,'TERD_START_TIME=', NEW.TERD_START_TIME,','); 
	END IF;

	IF (OLD.TERD_END_TIME IS NULL AND NEW.TERD_END_TIME IS NOT NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'TERD_END_TIME=','<NULL>,');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'TERD_END_TIME=',NEW.TERD_END_TIME,',');
	ELSEIF(OLD.TERD_END_TIME IS NOT NULL AND NEW.TERD_END_TIME IS NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'TERD_END_TIME=',OLD.TERD_END_TIME,',');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'TERD_END_TIME=','<NULL>,');
	ELSEIF (OLD.TERD_END_TIME!= NEW.TERD_END_TIME) THEN 
		SET OLD_VALUE = CONCAT(OLD_VALUE,'TERD_END_TIME=', OLD.TERD_END_TIME,','); 
		SET NEW_VALUE = CONCAT(NEW_VALUE,'TERD_END_TIME=', NEW.TERD_END_TIME,','); 
	END IF;

	IF (OLD.TERD_OT IS NULL AND NEW.TERD_OT IS NOT NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'TERD_OT=','<NULL>,');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'TERD_OT=',NEW.TERD_OT,',');
	ELSEIF(OLD.TERD_OT IS NOT NULL AND NEW.TERD_OT IS NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'TERD_OT=',OLD.TERD_OT,',');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'TERD_OT=','<NULL>,');
	ELSEIF (OLD.TERD_OT!= NEW.TERD_OT) THEN 
		SET OLD_VALUE = CONCAT(OLD_VALUE,'TERD_OT=', OLD.TERD_OT,','); 
		SET NEW_VALUE = CONCAT(NEW_VALUE,'TERD_OT=', NEW.TERD_OT,','); 
	END IF;

	IF (OLD.TERD_REMARK IS NULL AND NEW.TERD_REMARK IS NOT NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'TERD_REMARK=','<NULL>,');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'TERD_REMARK=',NEW.TERD_REMARK,',');
	ELSEIF(OLD.TERD_REMARK IS NOT NULL AND NEW.TERD_REMARK IS NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'TERD_REMARK=',OLD.TERD_REMARK,',');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'TERD_REMARK=','<NULL>,');
	ELSEIF (OLD.TERD_REMARK!= NEW.TERD_REMARK) THEN 
		SET OLD_VALUE = CONCAT(OLD_VALUE,'TERD_REMARK=', OLD.TERD_REMARK,','); 
		SET NEW_VALUE = CONCAT(NEW_VALUE,'TERD_REMARK=', NEW.TERD_REMARK,','); 
	END IF;
	
	IF((OLD.TRD_ID!= NEW.TRD_ID) OR (OLD.ULD_ID!= NEW.ULD_ID) OR
 	(OLD.TERD_START_TIME IS NULL AND NEW.TERD_START_TIME IS NOT NULL) OR
 	(OLD.TERD_START_TIME IS NOT NULL AND NEW.TERD_START_TIME IS NULL) OR
 	(OLD.TERD_START_TIME!=NEW.TERD_START_TIME) OR
 	(OLD.TERD_END_TIME IS NULL AND NEW.TERD_END_TIME IS NOT NULL) OR
 	(OLD.TERD_END_TIME IS NOT NULL AND NEW.TERD_END_TIME IS NULL) OR
 	(OLD.TERD_END_TIME!=NEW.TERD_END_TIME) OR
 	(OLD.TERD_OT IS NULL AND NEW.TERD_OT IS NOT NULL) OR
 	(OLD.TERD_OT IS NOT NULL AND NEW.TERD_OT IS NULL) OR
 	(OLD.TERD_OT!=NEW.TERD_OT) OR
 	(OLD.TERD_REMARK IS NULL AND NEW.TERD_REMARK IS NOT NULL) OR
 	(OLD.TERD_REMARK IS NOT NULL AND NEW.TERD_REMARK IS NULL) OR
 	(OLD.TERD_REMARK!=NEW.TERD_REMARK))THEN

		IF (OLD.TERD_USERSTAMP_ID!= NEW.TERD_USERSTAMP_ID) THEN  
			SET OLD_VALUE = CONCAT(OLD_VALUE,'TERD_USERSTAMP_ID=', OLD.TERD_USERSTAMP_ID,','); 
		END IF;

		IF (OLD.TERD_TIMESTAMP!= NEW.TERD_TIMESTAMP) THEN 
			SET OLD_VALUE = CONCAT(OLD_VALUE,'TERD_TIMESTAMP=', OLD.TERD_TIMESTAMP,','); 
		END IF;

	END IF;

	IF (OLD_VALUE!='' AND NEW_VALUE!='') THEN	

		IF(OLD_VALUE!=NEW_VALUE)THEN		

			SET OLD_VALUE = SUBSTRING(OLD_VALUE,1,CHAR_LENGTH(OLD_VALUE)-1);
			SET NEW_VALUE = SUBSTRING(NEW_VALUE,1,CHAR_LENGTH(NEW_VALUE)-1);			
			INSERT INTO LMC_TICKLER_HISTORY(TP_ID,ULD_ID,TTIP_ID,TH_OLD_VALUE,TH_NEW_VALUE,TH_USERSTAMP_ID)VALUES
			((SELECT TP_ID FROM LMC_TICKLER_PROFILE WHERE TP_TYPE='UPDATION'),(SELECT ULD_ID FROM LMC_TEAM_REPORT_DETAILS WHERE TRD_ID=NEW.TRD_ID),
			(SELECT TTIP_ID FROM LMC_TICKLER_TABID_PROFILE WHERE TTIP_DATA='LMC_TEAM_EMPLOYEE_REPORT_DETAILS'),OLD_VALUE,NEW_VALUE,
			(SELECT TERD_USERSTAMP_ID FROM LMC_TEAM_EMPLOYEE_REPORT_DETAILS WHERE TERD_ID=NEW.TERD_ID));
		
		END IF;	

	END IF;
	
END;