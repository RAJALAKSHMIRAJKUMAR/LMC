DROP TRIGGER IF EXISTS TRG_LMC_MATERIAL_USAGE_UPDATE;
CREATE TRIGGER TRG_LMC_MATERIAL_USAGE_UPDATE
AFTER UPDATE ON LMC_MATERIAL_USAGE
FOR EACH ROW
BEGIN 

	DECLARE OLD_VALUE TEXT DEFAULT '';
	DECLARE NEW_VALUE TEXT DEFAULT '';

	IF (OLD.MU_ITEMS!=NEW.MU_ITEMS) THEN
		SET OLD_VALUE = CONCAT(OLD_VALUE,'MU_ID=', OLD.MU_ID,','); 
	END IF;

	IF (OLD.MU_ITEMS!= NEW.MU_ITEMS) THEN 
		SET OLD_VALUE = CONCAT(OLD_VALUE,'MU_ITEMS=', OLD.MU_ITEMS,',');  
		SET NEW_VALUE = CONCAT(NEW_VALUE,'MU_ITEMS=', NEW.MU_ITEMS,','); 
	END IF;

	IF (OLD.MU_ITEMS!=NEW.MU_ITEMS) THEN	

		IF (OLD.ULD_ID!= NEW.ULD_ID) THEN  
			SET OLD_VALUE = CONCAT(OLD_VALUE,'ULD_ID=', OLD.ULD_ID,','); 
		END IF;

		IF (OLD.MU_TIMESTAMP!= NEW.MU_TIMESTAMP) THEN 
			SET OLD_VALUE = CONCAT(OLD_VALUE,'MU_TIMESTAMP=', OLD.MU_TIMESTAMP,','); 
		END IF;

	END IF;

	IF (OLD_VALUE!='' AND NEW_VALUE!='') THEN		
			
		IF(OLD_VALUE!=NEW_VALUE)THEN			
				
			SET OLD_VALUE = SUBSTRING(OLD_VALUE,1,CHAR_LENGTH(OLD_VALUE)-1);
			SET NEW_VALUE = SUBSTRING(NEW_VALUE,1,CHAR_LENGTH(NEW_VALUE)-1);			
				
			INSERT INTO LMC_TICKLER_HISTORY(TP_ID,TTIP_ID,TH_OLD_VALUE,TH_NEW_VALUE,TH_USERSTAMP_ID)VALUES
			((SELECT TP_ID FROM LMC_TICKLER_PROFILE WHERE TP_TYPE='UPDATION'),
			(SELECT TTIP_ID FROM LMC_TICKLER_TABID_PROFILE WHERE TTIP_DATA='LMC_MATERIAL_USAGE'),OLD_VALUE,NEW_VALUE,
			(SELECT ULD_ID FROM LMC_MATERIAL_USAGE WHERE MU_ID=NEW.MU_ID));
			
		END IF;		
		
	END IF;
	
END;