-- VER0.1 STARTDATE:29/01/2015 ENDDATE:29/01/2015 ISSUE:2 COMMENT NO:1 -- >desc:TRIGGER TO INSERT VALUES IN TICKLER HISTORY TABLE AFTER UPDATING IN SITE_VISIT_DETAILS TABLE --DONE BY:DHIVYA


DROP TRIGGER IF EXISTS TRG_LMC_SITE_VISIT_DETAILS_UPDATE;
CREATE TRIGGER TRG_LMC_SITE_VISIT_DETAILS_UPDATE
AFTER UPDATE ON LMC_SITE_VISIT_DETAILS
FOR EACH ROW

BEGIN 

	DECLARE OLD_VALUE TEXT DEFAULT '';
	DECLARE NEW_VALUE TEXT DEFAULT '';

	IF((OLD.TRD_ID!= NEW.TRD_ID) OR
	(OLD.SVD_NAME IS NULL AND NEW.SVD_NAME IS NOT NULL) OR
	(OLD.SVD_NAME IS NOT NULL AND NEW.SVD_NAME IS NULL) OR
	(OLD.SVD_NAME!=NEW.SVD_NAME) OR
	(OLD.SVD_DESIGNATION IS NULL AND NEW.SVD_DESIGNATION IS NOT NULL) OR
	(OLD.SVD_DESIGNATION IS NOT NULL AND NEW.SVD_DESIGNATION IS NULL) OR
	(OLD.SVD_DESIGNATION!=NEW.SVD_DESIGNATION) OR
	(OLD.SVD_START_TIME IS NULL AND NEW.SVD_START_TIME IS NOT NULL) OR
	(OLD.SVD_START_TIME IS NOT NULL AND NEW.SVD_START_TIME IS NULL) OR
	(OLD.SVD_START_TIME!=NEW.SVD_START_TIME) OR
	(OLD.SVD_END_TIME IS NULL AND NEW.SVD_END_TIME IS NOT NULL) OR
	(OLD.SVD_END_TIME IS NOT NULL AND NEW.SVD_END_TIME IS NULL) OR
	(OLD.SVD_END_TIME!=NEW.SVD_END_TIME) OR
	(OLD.SVD_REMARK IS NULL AND NEW.SVD_REMARK IS NOT NULL) OR
	(OLD.SVD_REMARK IS NOT NULL AND NEW.SVD_REMARK IS NULL) OR
	(OLD.SVD_REMARK!=NEW.SVD_REMARK))THEN
		SET OLD_VALUE = CONCAT(OLD_VALUE,'SVD_ID=', OLD.SVD_ID,','); 
	END IF;
	
	IF (OLD.TRD_ID!= NEW.TRD_ID) THEN  
		SET OLD_VALUE = CONCAT(OLD_VALUE,'TRD_ID=', OLD.TRD_ID,','); 
		SET NEW_VALUE = CONCAT(NEW_VALUE,'TRD_ID=', NEW.TRD_ID,','); 
	END IF;

	IF (OLD.SVD_NAME IS NULL AND NEW.SVD_NAME IS NOT NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'SVD_NAME=','<NULL>,');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'SVD_NAME=',NEW.SVD_NAME,',');
	ELSEIF(OLD.SVD_NAME IS NOT NULL AND NEW.SVD_NAME IS NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'SVD_NAME=',OLD.SVD_NAME,',');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'SVD_NAME=','<NULL>,');
	ELSEIF (OLD.SVD_NAME!= NEW.SVD_NAME) THEN 
		SET OLD_VALUE = CONCAT(OLD_VALUE,'SVD_NAME=', OLD.SVD_NAME,','); 
		SET NEW_VALUE = CONCAT(NEW_VALUE,'SVD_NAME=', NEW.SVD_NAME,','); 
	END IF;

	IF (OLD.SVD_DESIGNATION IS NULL AND NEW.SVD_DESIGNATION IS NOT NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'SVD_DESIGNATION=','<NULL>,');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'SVD_DESIGNATION=',NEW.SVD_DESIGNATION,',');
	ELSEIF(OLD.SVD_DESIGNATION IS NOT NULL AND NEW.SVD_DESIGNATION IS NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'SVD_DESIGNATION=',OLD.SVD_DESIGNATION,',');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'SVD_DESIGNATION=','<NULL>,');
	ELSEIF (OLD.SVD_DESIGNATION!= NEW.SVD_DESIGNATION) THEN 
		SET OLD_VALUE = CONCAT(OLD_VALUE,'SVD_DESIGNATION=', OLD.SVD_DESIGNATION,','); 
		SET NEW_VALUE = CONCAT(NEW_VALUE,'SVD_DESIGNATION=', NEW.SVD_DESIGNATION,','); 
	END IF;

	IF (OLD.SVD_START_TIME IS NULL AND NEW.SVD_START_TIME IS NOT NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'SVD_START_TIME=','<NULL>,');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'SVD_START_TIME=',NEW.SVD_START_TIME,',');
	ELSEIF(OLD.SVD_START_TIME IS NOT NULL AND NEW.SVD_START_TIME IS NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'SVD_START_TIME=',OLD.SVD_START_TIME,',');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'SVD_START_TIME=','<NULL>,');
	ELSEIF (OLD.SVD_START_TIME!= NEW.SVD_START_TIME) THEN 
		SET OLD_VALUE = CONCAT(OLD_VALUE,'SVD_START_TIME=', OLD.SVD_START_TIME,','); 
		SET NEW_VALUE = CONCAT(NEW_VALUE,'SVD_START_TIME=', NEW.SVD_START_TIME,','); 
	END IF;

	IF (OLD.SVD_END_TIME IS NULL AND NEW.SVD_END_TIME IS NOT NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'SVD_END_TIME=','<NULL>,');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'SVD_END_TIME=',NEW.SVD_END_TIME,',');
	ELSEIF(OLD.SVD_END_TIME IS NOT NULL AND NEW.SVD_END_TIME IS NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'SVD_END_TIME=',OLD.SVD_END_TIME,',');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'SVD_END_TIME=','<NULL>,');
	ELSEIF (OLD.SVD_END_TIME!= NEW.SVD_END_TIME) THEN 
		SET OLD_VALUE = CONCAT(OLD_VALUE,'SVD_END_TIME=', OLD.SVD_END_TIME,','); 
		SET NEW_VALUE = CONCAT(NEW_VALUE,'SVD_END_TIME=', NEW.SVD_END_TIME,','); 
	END IF;

	IF (OLD.SVD_REMARK IS NULL AND NEW.SVD_REMARK IS NOT NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'SVD_REMARK=','<NULL>,');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'SVD_REMARK=',NEW.SVD_REMARK,',');
	ELSEIF(OLD.SVD_REMARK IS NOT NULL AND NEW.SVD_REMARK IS NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'SVD_REMARK=',OLD.SVD_REMARK,',');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'SVD_REMARK=','<NULL>,');
	ELSEIF (OLD.SVD_REMARK!= NEW.SVD_REMARK) THEN 
		SET OLD_VALUE = CONCAT(OLD_VALUE,'SVD_REMARK=', OLD.SVD_REMARK,','); 
		SET NEW_VALUE = CONCAT(NEW_VALUE,'SVD_REMARK=', NEW.SVD_REMARK,','); 
	END IF;
	
	IF((OLD.TRD_ID!= NEW.TRD_ID) OR
	(OLD.SVD_NAME IS NULL AND NEW.SVD_NAME IS NOT NULL) OR
	(OLD.SVD_NAME IS NOT NULL AND NEW.SVD_NAME IS NULL) OR
	(OLD.SVD_NAME!=NEW.SVD_NAME) OR
	(OLD.SVD_DESIGNATION IS NULL AND NEW.SVD_DESIGNATION IS NOT NULL) OR
	(OLD.SVD_DESIGNATION IS NOT NULL AND NEW.SVD_DESIGNATION IS NULL) OR
	(OLD.SVD_DESIGNATION!=NEW.SVD_DESIGNATION) OR
	(OLD.SVD_START_TIME IS NULL AND NEW.SVD_START_TIME IS NOT NULL) OR
	(OLD.SVD_START_TIME IS NOT NULL AND NEW.SVD_START_TIME IS NULL) OR
	(OLD.SVD_START_TIME!=NEW.SVD_START_TIME) OR
	(OLD.SVD_END_TIME IS NULL AND NEW.SVD_END_TIME IS NOT NULL) OR
	(OLD.SVD_END_TIME IS NOT NULL AND NEW.SVD_END_TIME IS NULL) OR
	(OLD.SVD_END_TIME!=NEW.SVD_END_TIME) OR
	(OLD.SVD_REMARK IS NULL AND NEW.SVD_REMARK IS NOT NULL) OR
	(OLD.SVD_REMARK IS NOT NULL AND NEW.SVD_REMARK IS NULL) OR
	(OLD.SVD_REMARK!=NEW.SVD_REMARK))THEN

		IF (OLD.ULD_ID!= NEW.ULD_ID) THEN  
			SET OLD_VALUE = CONCAT(OLD_VALUE,'ULD_ID=', OLD.ULD_ID,','); 
		END IF;

		IF (OLD.SVD_TIMESTAMP!= NEW.SVD_TIMESTAMP) THEN 
			SET OLD_VALUE = CONCAT(OLD_VALUE,'SVD_TIMESTAMP=', OLD.SVD_TIMESTAMP,','); 
		END IF;

	END IF;

	IF (OLD_VALUE!='' AND NEW_VALUE!='') THEN

		IF(OLD_VALUE!=NEW_VALUE)THEN	

			SET OLD_VALUE = SUBSTRING(OLD_VALUE,1,CHAR_LENGTH(OLD_VALUE)-1);
			SET NEW_VALUE = SUBSTRING(NEW_VALUE,1,CHAR_LENGTH(NEW_VALUE)-1);			
			INSERT INTO LMC_TICKLER_HISTORY(TP_ID,ULD_ID,TTIP_ID,TH_OLD_VALUE,TH_NEW_VALUE,TH_USERSTAMP_ID)VALUES
			((SELECT TP_ID FROM LMC_TICKLER_PROFILE WHERE TP_TYPE='UPDATION'),(SELECT ULD_ID FROM LMC_TEAM_REPORT_DETAILS WHERE TRD_ID=NEW.TRD_ID),
			(SELECT TTIP_ID FROM LMC_TICKLER_TABID_PROFILE WHERE TTIP_DATA='LMC_SITE_VISIT_DETAILS'),OLD_VALUE,NEW_VALUE,
			(SELECT ULD_ID FROM LMC_SITE_VISIT_DETAILS WHERE SVD_ID=NEW.SVD_ID));

		END IF;	

	END IF;
	
END;