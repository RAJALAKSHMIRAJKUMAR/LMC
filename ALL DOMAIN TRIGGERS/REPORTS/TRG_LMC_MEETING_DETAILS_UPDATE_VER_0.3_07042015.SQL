DROP TRIGGER IF EXISTS TRG_LMC_MEETING_DETAILS_UPDATE;
CREATE TRIGGER TRG_LMC_MEETING_DETAILS_UPDATE
AFTER UPDATE ON LMC_MEETING_DETAILS
FOR EACH ROW

BEGIN 
	
	DECLARE OLD_VALUE TEXT DEFAULT '';
	DECLARE NEW_VALUE TEXT DEFAULT '';
	
	IF((OLD.TRD_ID!=NEW.TRD_ID) OR (OLD.MT_ID!=NEW.MT_ID) OR 
	(OLD.MD_REMARKS IS NULL AND NEW.MD_REMARKS IS NOT NULL) OR
	(OLD.MD_REMARKS IS NOT NULL AND NEW.MD_REMARKS IS NULL) OR
	(OLD.MD_REMARKS!=NEW.MD_REMARKS))THEN
		SET OLD_VALUE = CONCAT(OLD_VALUE,'MD_ID=', OLD.MD_ID,','); 
	END IF;

	IF (OLD.TRD_ID!= NEW.TRD_ID) THEN  
		SET OLD_VALUE = CONCAT(OLD_VALUE,'TRD_ID=', OLD.TRD_ID,','); 
		SET NEW_VALUE = CONCAT(NEW_VALUE,'TRD_ID=', NEW.TRD_ID,','); 
	END IF;

	IF (OLD.MT_ID!= NEW.MT_ID) THEN  
		SET OLD_VALUE = CONCAT(OLD_VALUE,'MT_ID=', OLD.MT_ID,','); 
		SET NEW_VALUE = CONCAT(NEW_VALUE,'MT_ID=', NEW.MT_ID,','); 
	END IF;

	IF (OLD.MD_REMARKS IS NULL AND NEW.MD_REMARKS IS NOT NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'MD_REMARKS=','<NULL>,');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'MD_REMARKS=',NEW.MD_REMARKS,',');
	ELSEIF(OLD.MD_REMARKS IS NOT NULL AND NEW.MD_REMARKS IS NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'MD_REMARKS=',OLD.MD_REMARKS,',');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'MD_REMARKS=','<NULL>,');
	ELSEIF (OLD.MD_REMARKS!= NEW.MD_REMARKS) THEN 
		SET OLD_VALUE = CONCAT(OLD_VALUE,'MD_REMARKS=', OLD.MD_REMARKS,','); 
		SET NEW_VALUE = CONCAT(NEW_VALUE,'MD_REMARKS=', NEW.MD_REMARKS,','); 
	END IF;
	
	IF((OLD.TRD_ID!=NEW.TRD_ID) OR (OLD.MT_ID!=NEW.MT_ID) OR 
	(OLD.MD_REMARKS IS NULL AND NEW.MD_REMARKS IS NOT NULL) OR
	(OLD.MD_REMARKS IS NOT NULL AND NEW.MD_REMARKS IS NULL) OR
	(OLD.MD_REMARKS!=NEW.MD_REMARKS))THEN

		IF (OLD.ULD_ID!= NEW.ULD_ID) THEN  
			SET OLD_VALUE = CONCAT(OLD_VALUE,'ULD_ID=', OLD.ULD_ID,','); 
		END IF;

		IF (OLD.MD_TIMESTAMP!= NEW.MD_TIMESTAMP) THEN 
			SET OLD_VALUE = CONCAT(OLD_VALUE,'MD_TIMESTAMP=', OLD.MD_TIMESTAMP,','); 
		END IF;
		
	END IF;

	IF (OLD_VALUE!='' AND NEW_VALUE!='') THEN	

		IF(OLD_VALUE!=NEW_VALUE)THEN	

			SET OLD_VALUE = SUBSTRING(OLD_VALUE,1,CHAR_LENGTH(OLD_VALUE)-1);
			SET NEW_VALUE = SUBSTRING(NEW_VALUE,1,CHAR_LENGTH(NEW_VALUE)-1);

			INSERT INTO LMC_TICKLER_HISTORY(TP_ID,TTIP_ID,ULD_ID,TH_OLD_VALUE,TH_NEW_VALUE,TH_USERSTAMP_ID)VALUES
			((SELECT TP_ID FROM LMC_TICKLER_PROFILE WHERE TP_TYPE='UPDATION'),
			(SELECT TTIP_ID FROM LMC_TICKLER_TABID_PROFILE WHERE TTIP_DATA='LMC_MEETING_DETAILS'),
			(SELECT ULD_ID FROM LMC_TEAM_REPORT_DETAILS WHERE TRD_ID=NEW.TRD_ID),OLD_VALUE,NEW_VALUE,
			NEW.ULD_ID);

		END IF;		

	END IF;
	
END;