DROP TRIGGER IF EXISTS TRG_LMC_TYPE_OF_JOB_UPDATE;
CREATE TRIGGER TRG_LMC_TYPE_OF_JOB_UPDATE
AFTER UPDATE ON LMC_TYPE_OF_JOB
FOR EACH ROW

BEGIN 
	
	DECLARE OLD_VALUE TEXT DEFAULT '';
	DECLARE NEW_VALUE TEXT DEFAULT '';
	
	IF (OLD.TOJ_JOB!=NEW.TOJ_JOB)  THEN
		SET OLD_VALUE = CONCAT(OLD_VALUE,'TOJ_ID=', OLD.TOJ_ID,','); 
	END IF;

	IF (OLD.TOJ_JOB!= NEW.TOJ_JOB) THEN  
		SET OLD_VALUE = CONCAT(OLD_VALUE,'TOJ_JOB=', OLD.TOJ_JOB,','); 
		SET NEW_VALUE = CONCAT(NEW_VALUE,'TOJ_JOB=', NEW.TOJ_JOB,','); 
	END IF;

	IF (OLD.TOJ_JOB!=NEW.TOJ_JOB)  THEN

		IF (OLD.ULD_ID!= NEW.ULD_ID) THEN  
			SET OLD_VALUE = CONCAT(OLD_VALUE,'ULD_ID=', OLD.ULD_ID,','); 
		END IF;

		IF (OLD.TOJ_TIMESTAMP!= NEW.TOJ_TIMESTAMP) THEN 
			SET OLD_VALUE = CONCAT(OLD_VALUE,'TOJ_TIMESTAMP=', OLD.TOJ_TIMESTAMP,','); 
		END IF;

	END IF;

	IF (OLD_VALUE!='' AND NEW_VALUE!='') THEN

		IF(OLD_VALUE!=NEW_VALUE)THEN	

			SET OLD_VALUE = SUBSTRING(OLD_VALUE,1,CHAR_LENGTH(OLD_VALUE)-1);
			SET NEW_VALUE = SUBSTRING(NEW_VALUE,1,CHAR_LENGTH(NEW_VALUE)-1);			
			INSERT INTO LMC_TICKLER_HISTORY(TP_ID,TTIP_ID,TH_OLD_VALUE,TH_NEW_VALUE,TH_USERSTAMP_ID)VALUES
			((SELECT TP_ID FROM LMC_TICKLER_PROFILE WHERE TP_TYPE='UPDATION'),
			(SELECT TTIP_ID FROM LMC_TICKLER_TABID_PROFILE WHERE TTIP_DATA='LMC_TYPE_OF_JOB'),OLD_VALUE,NEW_VALUE,
			NEW.ULD_ID);

		END IF;		

	END IF;
	
END;