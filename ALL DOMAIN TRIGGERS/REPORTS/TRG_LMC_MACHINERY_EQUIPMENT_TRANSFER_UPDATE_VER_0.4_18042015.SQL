-- VER0.1 STARTDATE:29/01/2015 ENDDATE:29/01/2015 ISSUE:2 COMMENT NO:29 -- >desc:TRIGGER TO INSERT VALUES IN TICKLER HISTORY TABLE AFTER UPDATING IN LMC_MACHINERY_EQUIPMENT_TRANSFER TABLE --DONE BY:DHIVYA


DROP TRIGGER IF EXISTS TRG_LMC_MACHINERY_EQUIPMENT_TRANSFER_UPDATE;
CREATE TRIGGER TRG_LMC_MACHINERY_EQUIPMENT_TRANSFER_UPDATE
AFTER UPDATE ON LMC_MACHINERY_EQUIPMENT_TRANSFER
FOR EACH ROW

BEGIN 

	DECLARE OLD_VALUE TEXT DEFAULT '';
	DECLARE NEW_VALUE TEXT DEFAULT '';

	IF((OLD.TRD_ID!= NEW.TRD_ID) OR
	(OLD.MET_FROM_LORRY_NO IS NULL AND NEW.MET_FROM_LORRY_NO IS NOT NULL) OR
	(OLD.MET_FROM_LORRY_NO IS NOT NULL AND NEW.MET_FROM_LORRY_NO IS NULL) OR
	(OLD.MET_FROM_LORRY_NO!=NEW.MET_FROM_LORRY_NO) OR
	(OLD.MET_TO_LORRY_NO IS NULL AND NEW.MET_TO_LORRY_NO IS NOT NULL) OR
	(OLD.MET_TO_LORRY_NO IS NOT NULL AND NEW.MET_TO_LORRY_NO IS NULL) OR
	(OLD.MET_TO_LORRY_NO!=NEW.MET_TO_LORRY_NO) OR
	(OLD.MI_ID!=NEW.MI_ID) OR
	(OLD.MET_REMARK IS NULL AND NEW.MET_REMARK IS NOT NULL) OR
	(OLD.MET_REMARK IS NOT NULL AND NEW.MET_REMARK IS NULL) OR
	(OLD.MET_REMARK!=NEW.MET_REMARK)) THEN
		SET OLD_VALUE = CONCAT(OLD_VALUE,'MET_ID=', OLD.MET_ID,','); 
	END IF;
	
	IF (OLD.TRD_ID!= NEW.TRD_ID) THEN  
		SET OLD_VALUE = CONCAT(OLD_VALUE,'TRD_ID=', OLD.TRD_ID,','); 
		SET NEW_VALUE = CONCAT(NEW_VALUE,'TRD_ID=', NEW.TRD_ID,','); 
	END IF;

	IF (OLD.MET_FROM_LORRY_NO IS NULL AND NEW.MET_FROM_LORRY_NO IS NOT NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'MET_FROM_LORRY_NO=','<NULL>,');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'MET_FROM_LORRY_NO=',NEW.MET_FROM_LORRY_NO,',');
	ELSEIF(OLD.MET_FROM_LORRY_NO IS NOT NULL AND NEW.MET_FROM_LORRY_NO IS NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'MET_FROM_LORRY_NO=',OLD.MET_FROM_LORRY_NO,',');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'MET_FROM_LORRY_NO=','<NULL>,');
	ELSEIF (OLD.MET_FROM_LORRY_NO!= NEW.MET_FROM_LORRY_NO) THEN 
		SET OLD_VALUE = CONCAT(OLD_VALUE,'MET_FROM_LORRY_NO=', OLD.MET_FROM_LORRY_NO,','); 
		SET NEW_VALUE = CONCAT(NEW_VALUE,'MET_FROM_LORRY_NO=', NEW.MET_FROM_LORRY_NO,','); 
	END IF;

	IF (OLD.MET_TO_LORRY_NO IS NULL AND NEW.MET_TO_LORRY_NO IS NOT NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'MET_TO_LORRY_NO=','<NULL>,');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'MET_TO_LORRY_NO=',NEW.MET_TO_LORRY_NO,',');
	ELSEIF(OLD.MET_TO_LORRY_NO IS NOT NULL AND NEW.MET_TO_LORRY_NO IS NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'MET_TO_LORRY_NO=',OLD.MET_TO_LORRY_NO,',');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'MET_TO_LORRY_NO=','<NULL>,');
	ELSEIF (OLD.MET_TO_LORRY_NO!= NEW.MET_TO_LORRY_NO) THEN 
		SET OLD_VALUE = CONCAT(OLD_VALUE,'MET_TO_LORRY_NO=', OLD.MET_TO_LORRY_NO,','); 
		SET NEW_VALUE = CONCAT(NEW_VALUE,'MET_TO_LORRY_NO=', NEW.MET_TO_LORRY_NO,','); 
	END IF;

	IF (OLD.MI_ID!= NEW.MI_ID) THEN 
		SET OLD_VALUE = CONCAT(OLD_VALUE,'MI_ID=', OLD.MI_ID,','); 
		SET NEW_VALUE = CONCAT(NEW_VALUE,'MI_ID=', NEW.MI_ID,','); 
	END IF;

	IF (OLD.MET_REMARK IS NULL AND NEW.MET_REMARK IS NOT NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'MET_REMARK=','<NULL>,');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'MET_REMARK=',NEW.MET_REMARK,',');
	ELSEIF(OLD.MET_REMARK IS NOT NULL AND NEW.MET_REMARK IS NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'MET_REMARK=',OLD.MET_REMARK,',');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'MET_REMARK=','<NULL>,');
	ELSEIF (OLD.MET_REMARK!= NEW.MET_REMARK) THEN 
		SET OLD_VALUE = CONCAT(OLD_VALUE,'MET_REMARK=', OLD.MET_REMARK,','); 
		SET NEW_VALUE = CONCAT(NEW_VALUE,'MET_REMARK=', NEW.MET_REMARK,','); 
	END IF;
	
	IF((OLD.TRD_ID!= NEW.TRD_ID) OR
	(OLD.MET_FROM_LORRY_NO IS NULL AND NEW.MET_FROM_LORRY_NO IS NOT NULL) OR
	(OLD.MET_FROM_LORRY_NO IS NOT NULL AND NEW.MET_FROM_LORRY_NO IS NULL) OR
	(OLD.MET_FROM_LORRY_NO!=NEW.MET_FROM_LORRY_NO) OR
	(OLD.MET_TO_LORRY_NO IS NULL AND NEW.MET_TO_LORRY_NO IS NOT NULL) OR
	(OLD.MET_TO_LORRY_NO IS NOT NULL AND NEW.MET_TO_LORRY_NO IS NULL) OR
	(OLD.MET_TO_LORRY_NO!=NEW.MET_TO_LORRY_NO) OR
	(OLD.MI_ID!=NEW.MI_ID) OR
	(OLD.MET_REMARK IS NULL AND NEW.MET_REMARK IS NOT NULL) OR
	(OLD.MET_REMARK IS NOT NULL AND NEW.MET_REMARK IS NULL) OR
	(OLD.MET_REMARK!=NEW.MET_REMARK)) THEN

		IF (OLD.ULD_ID!= NEW.ULD_ID) THEN  
			SET OLD_VALUE = CONCAT(OLD_VALUE,'ULD_ID=', OLD.ULD_ID,','); 
		END IF;

		IF (OLD.MET_TIMESTAMP!= NEW.MET_TIMESTAMP) THEN 
			SET OLD_VALUE = CONCAT(OLD_VALUE,'MET_TIMESTAMP=', OLD.MET_TIMESTAMP,','); 
		END IF;

	END IF;

	IF (OLD_VALUE!='' AND NEW_VALUE!='') THEN	

		IF(OLD_VALUE!=NEW_VALUE)THEN	

			SET OLD_VALUE = SUBSTRING(OLD_VALUE,1,CHAR_LENGTH(OLD_VALUE)-1);
			SET NEW_VALUE = SUBSTRING(NEW_VALUE,1,CHAR_LENGTH(NEW_VALUE)-1);			
			INSERT INTO LMC_TICKLER_HISTORY(TP_ID,ULD_ID,TTIP_ID,TH_OLD_VALUE,TH_NEW_VALUE,TH_USERSTAMP_ID)VALUES
			((SELECT TP_ID FROM LMC_TICKLER_PROFILE WHERE TP_TYPE='UPDATION'),(SELECT ULD_ID FROM LMC_TEAM_REPORT_DETAILS WHERE TRD_ID=NEW.TRD_ID),
			(SELECT TTIP_ID FROM LMC_TICKLER_TABID_PROFILE WHERE TTIP_DATA='LMC_MACHINERY_EQUIPMENT_TRANSFER'),OLD_VALUE,NEW_VALUE,
			(SELECT ULD_ID FROM LMC_MACHINERY_EQUIPMENT_TRANSFER WHERE MET_ID=NEW.MET_ID));
		
		END IF;

	END IF;
	
END;