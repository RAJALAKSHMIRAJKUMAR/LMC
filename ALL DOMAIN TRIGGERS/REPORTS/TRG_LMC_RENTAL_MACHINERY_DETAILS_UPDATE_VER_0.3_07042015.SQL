-- VERSION:0.2 --DATE:26/03/2015 --DESC:EMP_ID CHANGED AS ULD_ID IN TICKLER HISTORY,LMC_TEAM_REPORT_DETAILS --DONE BY:RL

DROP TRIGGER IF EXISTS TRG_LMC_RENTAL_MACHINERY_DETAILS_UPDATE;
CREATE TRIGGER TRG_LMC_RENTAL_MACHINERY_DETAILS_UPDATE
AFTER UPDATE ON LMC_RENTAL_MACHINERY_DETAILS
FOR EACH ROW

BEGIN 

	DECLARE OLD_VALUE TEXT DEFAULT '';
	DECLARE NEW_VALUE TEXT DEFAULT '';

	IF((OLD.TRD_ID!= NEW.TRD_ID) OR 
	(OLD.RMD_LORRY_NO IS NULL AND NEW.RMD_LORRY_NO IS NOT NULL) OR
	(OLD.RMD_LORRY_NO IS NOT NULL AND NEW.RMD_LORRY_NO IS NULL) OR
	(OLD.RMD_LORRY_NO!=NEW.RMD_LORRY_NO) OR
	(OLD.RMD_THROWEARTH_STORE IS NULL AND NEW.RMD_THROWEARTH_STORE IS NOT NULL) OR
	(OLD.RMD_THROWEARTH_STORE IS NOT NULL AND NEW.RMD_THROWEARTH_STORE IS NULL) OR
	(OLD.RMD_THROWEARTH_STORE!=NEW.RMD_THROWEARTH_STORE) OR
	(OLD.RMD_THROWEARTH_OUTSIDE IS NULL AND NEW.RMD_THROWEARTH_OUTSIDE IS NOT NULL) OR
	(OLD.RMD_THROWEARTH_OUTSIDE IS NOT NULL AND NEW.RMD_THROWEARTH_OUTSIDE IS NULL) OR
	(OLD.RMD_THROWEARTH_OUTSIDE!=NEW.RMD_THROWEARTH_OUTSIDE) OR
	(OLD.RMD_START_TIME IS NULL AND NEW.RMD_START_TIME IS NOT NULL) OR
	(OLD.RMD_START_TIME IS NOT NULL AND NEW.RMD_START_TIME IS NULL) OR
	(OLD.RMD_START_TIME!=NEW.RMD_START_TIME) OR
	(OLD.RMD_END_TIME IS NULL AND NEW.RMD_END_TIME IS NOT NULL) OR
	(OLD.RMD_END_TIME IS NOT NULL AND NEW.RMD_END_TIME IS NULL) OR
	(OLD.RMD_END_TIME!=NEW.RMD_END_TIME) OR
	(OLD.RMD_REMARK IS NULL AND NEW.RMD_REMARK IS NOT NULL) OR
	(OLD.RMD_REMARK IS NOT NULL AND NEW.RMD_REMARK IS NULL) OR
	(OLD.RMD_REMARK!=NEW.RMD_REMARK)) THEN
		SET OLD_VALUE = CONCAT(OLD_VALUE,'RMD_ID=', OLD.RMD_ID,','); 
	END IF;
	
	IF (OLD.TRD_ID!= NEW.TRD_ID) THEN  
		SET OLD_VALUE = CONCAT(OLD_VALUE,'TRD_ID=', OLD.TRD_ID,','); 
		SET NEW_VALUE = CONCAT(NEW_VALUE,'TRD_ID=', NEW.TRD_ID,','); 
	END IF;

	IF (OLD.RMD_LORRY_NO IS NULL AND NEW.RMD_LORRY_NO IS NOT NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'RMD_LORRY_NO=','<NULL>,');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'RMD_LORRY_NO=',NEW.RMD_LORRY_NO,',');
	ELSEIF(OLD.RMD_LORRY_NO IS NOT NULL AND NEW.RMD_LORRY_NO IS NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'RMD_LORRY_NO=',OLD.RMD_LORRY_NO,',');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'RMD_LORRY_NO=','<NULL>,');
	ELSEIF (OLD.RMD_LORRY_NO!= NEW.RMD_LORRY_NO) THEN 
		SET OLD_VALUE = CONCAT(OLD_VALUE,'RMD_LORRY_NO=', OLD.RMD_LORRY_NO,','); 
		SET NEW_VALUE = CONCAT(NEW_VALUE,'RMD_LORRY_NO=', NEW.RMD_LORRY_NO,','); 
	END IF;

	IF (OLD.RMD_THROWEARTH_STORE IS NULL AND NEW.RMD_THROWEARTH_STORE IS NOT NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'RMD_THROWEARTH_STORE=','<NULL>,');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'RMD_THROWEARTH_STORE=',NEW.RMD_THROWEARTH_STORE,',');
	ELSEIF(OLD.RMD_THROWEARTH_STORE IS NOT NULL AND NEW.RMD_THROWEARTH_STORE IS NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'RMD_THROWEARTH_STORE=',OLD.RMD_THROWEARTH_STORE,',');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'RMD_THROWEARTH_STORE=','<NULL>,');
	ELSEIF (OLD.RMD_THROWEARTH_STORE!= NEW.RMD_THROWEARTH_STORE) THEN 
		SET OLD_VALUE = CONCAT(OLD_VALUE,'RMD_THROWEARTH_STORE=', OLD.RMD_THROWEARTH_STORE,','); 
		SET NEW_VALUE = CONCAT(NEW_VALUE,'RMD_THROWEARTH_STORE=', NEW.RMD_THROWEARTH_STORE,','); 
	END IF;

	IF (OLD.RMD_THROWEARTH_OUTSIDE IS NULL AND NEW.RMD_THROWEARTH_OUTSIDE IS NOT NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'RMD_THROWEARTH_OUTSIDE=','<NULL>,');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'RMD_THROWEARTH_OUTSIDE=',NEW.RMD_THROWEARTH_OUTSIDE,',');
	ELSEIF(OLD.RMD_THROWEARTH_OUTSIDE IS NOT NULL AND NEW.RMD_THROWEARTH_OUTSIDE IS NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'RMD_THROWEARTH_OUTSIDE=',OLD.RMD_THROWEARTH_OUTSIDE,',');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'RMD_THROWEARTH_OUTSIDE=','<NULL>,');
	ELSEIF (OLD.RMD_THROWEARTH_OUTSIDE!= NEW.RMD_THROWEARTH_OUTSIDE) THEN 
		SET OLD_VALUE = CONCAT(OLD_VALUE,'RMD_THROWEARTH_OUTSIDE=', OLD.RMD_THROWEARTH_OUTSIDE,','); 
		SET NEW_VALUE = CONCAT(NEW_VALUE,'RMD_THROWEARTH_OUTSIDE=', NEW.RMD_THROWEARTH_OUTSIDE,','); 
	END IF;

	IF (OLD.RMD_START_TIME IS NULL AND NEW.RMD_START_TIME IS NOT NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'RMD_START_TIME=','<NULL>,');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'RMD_START_TIME=',NEW.RMD_START_TIME,',');
	ELSEIF(OLD.RMD_START_TIME IS NOT NULL AND NEW.RMD_START_TIME IS NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'RMD_START_TIME=',OLD.RMD_START_TIME,',');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'RMD_START_TIME=','<NULL>,');
	ELSEIF (OLD.RMD_START_TIME!= NEW.RMD_START_TIME) THEN 
		SET OLD_VALUE = CONCAT(OLD_VALUE,'RMD_START_TIME=', OLD.RMD_START_TIME,','); 
		SET NEW_VALUE = CONCAT(NEW_VALUE,'RMD_START_TIME=', NEW.RMD_START_TIME,','); 
	END IF;
	
	IF (OLD.RMD_END_TIME IS NULL AND NEW.RMD_END_TIME IS NOT NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'RMD_END_TIME=','<NULL>,');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'RMD_END_TIME=',NEW.RMD_END_TIME,',');
	ELSEIF(OLD.RMD_END_TIME IS NOT NULL AND NEW.RMD_END_TIME IS NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'RMD_END_TIME=',OLD.RMD_END_TIME,',');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'RMD_END_TIME=','<NULL>,');
	ELSEIF (OLD.RMD_END_TIME!= NEW.RMD_END_TIME) THEN 
		SET OLD_VALUE = CONCAT(OLD_VALUE,'RMD_END_TIME=', OLD.RMD_END_TIME,','); 
		SET NEW_VALUE = CONCAT(NEW_VALUE,'RMD_END_TIME=', NEW.RMD_END_TIME,','); 
	END IF;

	IF (OLD.RMD_REMARK IS NULL AND NEW.RMD_REMARK IS NOT NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'RMD_REMARK=','<NULL>,');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'RMD_REMARK=',NEW.RMD_REMARK,',');
	ELSEIF(OLD.RMD_REMARK IS NOT NULL AND NEW.RMD_REMARK IS NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'RMD_REMARK=',OLD.RMD_REMARK,',');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'RMD_REMARK=','<NULL>,');
	ELSEIF (OLD.RMD_REMARK!= NEW.RMD_REMARK) THEN 
		SET OLD_VALUE = CONCAT(OLD_VALUE,'RMD_REMARK=', OLD.RMD_REMARK,','); 
		SET NEW_VALUE = CONCAT(NEW_VALUE,'RMD_REMARK=', NEW.RMD_REMARK,','); 
	END IF;
	
	IF((OLD.TRD_ID!= NEW.TRD_ID) OR 
	(OLD.RMD_LORRY_NO IS NULL AND NEW.RMD_LORRY_NO IS NOT NULL) OR
	(OLD.RMD_LORRY_NO IS NOT NULL AND NEW.RMD_LORRY_NO IS NULL) OR
	(OLD.RMD_LORRY_NO!=NEW.RMD_LORRY_NO) OR
	(OLD.RMD_THROWEARTH_STORE IS NULL AND NEW.RMD_THROWEARTH_STORE IS NOT NULL) OR
	(OLD.RMD_THROWEARTH_STORE IS NOT NULL AND NEW.RMD_THROWEARTH_STORE IS NULL) OR
	(OLD.RMD_THROWEARTH_STORE!=NEW.RMD_THROWEARTH_STORE) OR
	(OLD.RMD_THROWEARTH_OUTSIDE IS NULL AND NEW.RMD_THROWEARTH_OUTSIDE IS NOT NULL) OR
	(OLD.RMD_THROWEARTH_OUTSIDE IS NOT NULL AND NEW.RMD_THROWEARTH_OUTSIDE IS NULL) OR
	(OLD.RMD_THROWEARTH_OUTSIDE!=NEW.RMD_THROWEARTH_OUTSIDE) OR
	(OLD.RMD_START_TIME IS NULL AND NEW.RMD_START_TIME IS NOT NULL) OR
	(OLD.RMD_START_TIME IS NOT NULL AND NEW.RMD_START_TIME IS NULL) OR
	(OLD.RMD_START_TIME!=NEW.RMD_START_TIME) OR
	(OLD.RMD_END_TIME IS NULL AND NEW.RMD_END_TIME IS NOT NULL) OR
	(OLD.RMD_END_TIME IS NOT NULL AND NEW.RMD_END_TIME IS NULL) OR
	(OLD.RMD_END_TIME!=NEW.RMD_END_TIME) OR
	(OLD.RMD_REMARK IS NULL AND NEW.RMD_REMARK IS NOT NULL) OR
	(OLD.RMD_REMARK IS NOT NULL AND NEW.RMD_REMARK IS NULL) OR
	(OLD.RMD_REMARK!=NEW.RMD_REMARK)) THEN

		IF (OLD.ULD_ID!= NEW.ULD_ID) THEN  
			SET OLD_VALUE = CONCAT(OLD_VALUE,'ULD_ID=', OLD.ULD_ID,','); 
		END IF;

		IF (OLD.RMD_TIMESTAMP!= NEW.RMD_TIMESTAMP) THEN 
			SET OLD_VALUE = CONCAT(OLD_VALUE,'RMD_TIMESTAMP=', OLD.RMD_TIMESTAMP,','); 
		END IF;

	END IF;

	IF (OLD_VALUE!='' AND NEW_VALUE!='') THEN

		IF(OLD_VALUE!=NEW_VALUE)THEN	

			SET OLD_VALUE = SUBSTRING(OLD_VALUE,1,CHAR_LENGTH(OLD_VALUE)-1);
			SET NEW_VALUE = SUBSTRING(NEW_VALUE,1,CHAR_LENGTH(NEW_VALUE)-1);			
			INSERT INTO LMC_TICKLER_HISTORY(TP_ID,ULD_ID,TTIP_ID,TH_OLD_VALUE,TH_NEW_VALUE,TH_USERSTAMP_ID)VALUES
			((SELECT TP_ID FROM LMC_TICKLER_PROFILE WHERE TP_TYPE='UPDATION'),(SELECT ULD_ID FROM LMC_TEAM_REPORT_DETAILS WHERE TRD_ID=NEW.TRD_ID),
			(SELECT TTIP_ID FROM LMC_TICKLER_TABID_PROFILE WHERE TTIP_DATA='LMC_RENTAL_MACHINERY_DETAILS'),OLD_VALUE,NEW_VALUE,
			(SELECT ULD_ID FROM LMC_RENTAL_MACHINERY_DETAILS WHERE RMD_ID=NEW.RMD_ID));
		
		END IF;	

	END IF;
	
END;