-- VER0.1 STARTDATE:29/01/2015 ENDDATE:29/01/2015 ISSUE:2 COMMENT NO:29 -- >desc:TRIGGER TO INSERT VALUES IN TICKLER HISTORY TABLE AFTER UPDATING IN LMC_MACHINERY_USAGE_DETAILS TABLE --DONE BY:DHIVYA


DROP TRIGGER IF EXISTS TRG_LMC_MACHINERY_USAGE_DETAILS_UPDATE;
CREATE TRIGGER TRG_LMC_MACHINERY_USAGE_DETAILS_UPDATE
AFTER UPDATE ON LMC_MACHINERY_USAGE_DETAILS
FOR EACH ROW

BEGIN 

	DECLARE OLD_VALUE TEXT DEFAULT '';
	DECLARE NEW_VALUE TEXT DEFAULT '';

	IF((OLD.TRD_ID!= NEW.TRD_ID) OR (OLD.MCU_ID!= NEW.MCU_ID) OR
 	(OLD.MAC_START_TIME IS NULL AND NEW.MAC_START_TIME IS NOT NULL) OR
 	(OLD.MAC_START_TIME IS NOT NULL AND NEW.MAC_START_TIME IS NULL) OR
 	(OLD.MAC_START_TIME!=NEW.MAC_START_TIME) OR
 	(OLD.MAC_END_TIME IS NULL AND NEW.MAC_END_TIME IS NOT NULL) OR
 	(OLD.MAC_END_TIME IS NOT NULL AND NEW.MAC_END_TIME IS NULL) OR
 	(OLD.MAC_END_TIME!=NEW.MAC_END_TIME) OR
 	(OLD.MAC_REMARK IS NULL AND NEW.MAC_REMARK IS NOT NULL) OR
 	(OLD.MAC_REMARK IS NOT NULL AND NEW.MAC_REMARK IS NULL) OR
 	(OLD.MAC_REMARK!=NEW.MAC_REMARK)) THEN
		SET OLD_VALUE = CONCAT(OLD_VALUE,'MAC_ID=', OLD.MAC_ID,','); 
	END IF;
	
	IF (OLD.TRD_ID!= NEW.TRD_ID) THEN  
		SET OLD_VALUE = CONCAT(OLD_VALUE,'TRD_ID=', OLD.TRD_ID,','); 
		SET NEW_VALUE = CONCAT(NEW_VALUE,'TRD_ID=', NEW.TRD_ID,','); 
	END IF;

	IF (OLD.MCU_ID!= NEW.MCU_ID) THEN 
		SET OLD_VALUE = CONCAT(OLD_VALUE,'MCU_ID=', OLD.MCU_ID,','); 
		SET NEW_VALUE = CONCAT(NEW_VALUE,'MCU_ID=', NEW.MCU_ID,','); 
	END IF;

	IF (OLD.MAC_START_TIME IS NULL AND NEW.MAC_START_TIME IS NOT NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'MAC_START_TIME=','<NULL>,');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'MAC_START_TIME=',NEW.MAC_START_TIME,',');
	ELSEIF(OLD.MAC_START_TIME IS NOT NULL AND NEW.MAC_START_TIME IS NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'MAC_START_TIME=',OLD.MAC_START_TIME,',');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'MAC_START_TIME=','<NULL>,');
	ELSEIF (OLD.MAC_START_TIME!= NEW.MAC_START_TIME) THEN 
		SET OLD_VALUE = CONCAT(OLD_VALUE,'MAC_START_TIME=', OLD.MAC_START_TIME,','); 
		SET NEW_VALUE = CONCAT(NEW_VALUE,'MAC_START_TIME=', NEW.MAC_START_TIME,','); 
	END IF;

	IF (OLD.MAC_END_TIME IS NULL AND NEW.MAC_END_TIME IS NOT NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'MAC_END_TIME=','<NULL>,');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'MAC_END_TIME=',NEW.MAC_END_TIME,',');
	ELSEIF(OLD.MAC_END_TIME IS NOT NULL AND NEW.MAC_END_TIME IS NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'MAC_END_TIME=',OLD.MAC_END_TIME,',');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'MAC_END_TIME=','<NULL>,');
	ELSEIF (OLD.MAC_END_TIME!= NEW.MAC_END_TIME) THEN 
		SET OLD_VALUE = CONCAT(OLD_VALUE,'MAC_END_TIME=', OLD.MAC_END_TIME,','); 
		SET NEW_VALUE = CONCAT(NEW_VALUE,'MAC_END_TIME=', NEW.MAC_END_TIME,','); 
	END IF;

	IF (OLD.MAC_REMARK IS NULL AND NEW.MAC_REMARK IS NOT NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'MAC_REMARK=','<NULL>,');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'MAC_REMARK=',NEW.MAC_REMARK,',');
	ELSEIF(OLD.MAC_REMARK IS NOT NULL AND NEW.MAC_REMARK IS NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'MAC_REMARK=',OLD.MAC_REMARK,',');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'MAC_REMARK=','<NULL>,');
	ELSEIF (OLD.MAC_REMARK!= NEW.MAC_REMARK) THEN 
		SET OLD_VALUE = CONCAT(OLD_VALUE,'MAC_REMARK=', OLD.MAC_REMARK,','); 
		SET NEW_VALUE = CONCAT(NEW_VALUE,'MAC_REMARK=', NEW.MAC_REMARK,','); 
	END IF;
	
	IF((OLD.TRD_ID!= NEW.TRD_ID) OR (OLD.MCU_ID!= NEW.MCU_ID) OR
 	(OLD.MAC_START_TIME IS NULL AND NEW.MAC_START_TIME IS NOT NULL) OR
 	(OLD.MAC_START_TIME IS NOT NULL AND NEW.MAC_START_TIME IS NULL) OR
 	(OLD.MAC_START_TIME!=NEW.MAC_START_TIME) OR
 	(OLD.MAC_END_TIME IS NULL AND NEW.MAC_END_TIME IS NOT NULL) OR
 	(OLD.MAC_END_TIME IS NOT NULL AND NEW.MAC_END_TIME IS NULL) OR
 	(OLD.MAC_END_TIME!=NEW.MAC_END_TIME) OR
 	(OLD.MAC_REMARK IS NULL AND NEW.MAC_REMARK IS NOT NULL) OR
 	(OLD.MAC_REMARK IS NOT NULL AND NEW.MAC_REMARK IS NULL) OR
 	(OLD.MAC_REMARK!=NEW.MAC_REMARK)) THEN

		IF (OLD.ULD_ID!= NEW.ULD_ID) THEN  
			SET OLD_VALUE = CONCAT(OLD_VALUE,'ULD_ID=', OLD.ULD_ID,','); 
		END IF;

		IF (OLD.MAC_TIMESTAMP!= NEW.MAC_TIMESTAMP) THEN 
			SET OLD_VALUE = CONCAT(OLD_VALUE,'MAC_TIMESTAMP=', OLD.MAC_TIMESTAMP,','); 
		END IF;

	END IF;

	IF (OLD_VALUE!='' AND NEW_VALUE!='') THEN

		IF(OLD_VALUE!=NEW_VALUE)THEN	

			SET OLD_VALUE = SUBSTRING(OLD_VALUE,1,CHAR_LENGTH(OLD_VALUE)-1);
			SET NEW_VALUE = SUBSTRING(NEW_VALUE,1,CHAR_LENGTH(NEW_VALUE)-1);			
			INSERT INTO LMC_TICKLER_HISTORY(TP_ID,ULD_ID,TTIP_ID,TH_OLD_VALUE,TH_NEW_VALUE,TH_USERSTAMP_ID)VALUES
			((SELECT TP_ID FROM LMC_TICKLER_PROFILE WHERE TP_TYPE='UPDATION'),(SELECT ULD_ID FROM LMC_TEAM_REPORT_DETAILS WHERE TRD_ID=NEW.TRD_ID),
			(SELECT TTIP_ID FROM LMC_TICKLER_TABID_PROFILE WHERE TTIP_DATA='LMC_MACHINERY_USAGE_DETAILS'),OLD_VALUE,NEW_VALUE,
			(SELECT ULD_ID FROM LMC_MACHINERY_USAGE_DETAILS WHERE MAC_ID=NEW.MAC_ID));
		
		END IF;	

	END IF;
	
END;