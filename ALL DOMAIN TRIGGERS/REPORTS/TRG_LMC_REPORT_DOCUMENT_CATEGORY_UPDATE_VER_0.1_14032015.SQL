DROP TRIGGER IF EXISTS TRG_LMC_REPORT_DOCUMENT_CATEGORY_UPDATE;
CREATE TRIGGER TRG_LMC_REPORT_DOCUMENT_CATEGORY_UPDATE
AFTER UPDATE ON LMC_REPORT_DOCUMENT_CATEGORY
FOR EACH ROW

BEGIN 
	
	DECLARE OLD_VALUE TEXT DEFAULT '';
	DECLARE NEW_VALUE TEXT DEFAULT '';
	
	IF(OLD.RDC_CATEGORY!=NEW.RDC_CATEGORY) THEN
		SET OLD_VALUE = CONCAT(OLD_VALUE,'RDC_ID=', OLD.RDC_ID,','); 
	END IF;

	IF (OLD.RDC_CATEGORY!= NEW.RDC_CATEGORY) THEN  
		SET OLD_VALUE = CONCAT(OLD_VALUE,'RDC_CATEGORY=', OLD.RDC_CATEGORY,','); 
		SET NEW_VALUE = CONCAT(NEW_VALUE,'RDC_CATEGORY=', NEW.RDC_CATEGORY,','); 
	END IF;

	IF(OLD.RDC_CATEGORY!=NEW.RDC_CATEGORY) THEN

		IF (OLD.ULD_ID!= NEW.ULD_ID) THEN  
			SET OLD_VALUE = CONCAT(OLD_VALUE,'ULD_ID=', OLD.ULD_ID,','); 
		END IF;

		IF (OLD.RDC_TIMESTAMP!= NEW.RDC_TIMESTAMP) THEN 
			SET OLD_VALUE = CONCAT(OLD_VALUE,'RDC_TIMESTAMP=', OLD.RDC_TIMESTAMP,','); 
		END IF;
		
	END IF;

	IF (OLD_VALUE!='' AND NEW_VALUE!='') THEN	

		IF(OLD_VALUE!=NEW_VALUE)THEN	

			SET OLD_VALUE = SUBSTRING(OLD_VALUE,1,CHAR_LENGTH(OLD_VALUE)-1);
			SET NEW_VALUE = SUBSTRING(NEW_VALUE,1,CHAR_LENGTH(NEW_VALUE)-1);

			INSERT INTO LMC_TICKLER_HISTORY(TP_ID,TTIP_ID,TH_OLD_VALUE,TH_NEW_VALUE,TH_USERSTAMP_ID)VALUES
			((SELECT TP_ID FROM LMC_TICKLER_PROFILE WHERE TP_TYPE='UPDATION'),
			(SELECT TTIP_ID FROM LMC_TICKLER_TABID_PROFILE WHERE TTIP_DATA='LMC_REPORT_DOCUMENT_CATEGORY'),
			OLD_VALUE,NEW_VALUE,NEW.ULD_ID);

		END IF;		

	END IF;
	
END;