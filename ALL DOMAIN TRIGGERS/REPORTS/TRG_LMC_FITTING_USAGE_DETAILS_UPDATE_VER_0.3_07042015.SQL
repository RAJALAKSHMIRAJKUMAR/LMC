-- VER0.1 STARTDATE:29/01/2015 ENDDATE:29/01/2015 ISSUE:2 COMMENT NO:1 -- >desc:TRIGGER TO INSERT VALUES IN TICKLER HISTORY TABLE AFTER UPDATING IN FITTING_USAGE_DETAILS TABLE --DONE BY:DHIVYA


DROP TRIGGER IF EXISTS TRG_LMC_FITTING_USAGE_DETAILS_UPDATE;
CREATE TRIGGER TRG_LMC_FITTING_USAGE_DETAILS_UPDATE
AFTER UPDATE ON LMC_FITTING_USAGE_DETAILS
FOR EACH ROW

BEGIN 

	DECLARE OLD_VALUE TEXT DEFAULT '';
	DECLARE NEW_VALUE TEXT DEFAULT '';

	IF((OLD.TRD_ID!= NEW.TRD_ID) OR (OLD.FU_ID!= NEW.FU_ID) OR
 	(OLD.FUD_SIZE IS NULL AND NEW.FUD_SIZE IS NOT NULL) OR
 	(OLD.FUD_SIZE IS NOT NULL AND NEW.FUD_SIZE IS NULL) OR
 	(OLD.FUD_SIZE!=NEW.FUD_SIZE) OR
 	(OLD.FUD_QUANTITY IS NULL AND NEW.FUD_QUANTITY IS NOT NULL) OR
 	(OLD.FUD_QUANTITY IS NOT NULL AND NEW.FUD_QUANTITY IS NULL) OR
 	(OLD.FUD_QUANTITY!=NEW.FUD_QUANTITY) OR
 	(OLD.FUD_REMARK IS NULL AND NEW.FUD_REMARK IS NOT NULL) OR
 	(OLD.FUD_REMARK IS NOT NULL AND NEW.FUD_REMARK IS NULL) OR
 	(OLD.FUD_REMARK!=NEW.FUD_REMARK)) THEN
		SET OLD_VALUE = CONCAT(OLD_VALUE,'FUD_ID=', OLD.FUD_ID,','); 
	END IF;
	
	IF (OLD.TRD_ID!= NEW.TRD_ID) THEN  
		SET OLD_VALUE = CONCAT(OLD_VALUE,'TRD_ID=', OLD.TRD_ID,','); 
		SET NEW_VALUE = CONCAT(NEW_VALUE,'TRD_ID=', NEW.TRD_ID,','); 
	END IF;

	IF (OLD.FU_ID!= NEW.FU_ID) THEN 
		SET OLD_VALUE = CONCAT(OLD_VALUE,'FU_ID=', OLD.FU_ID,','); 
		SET NEW_VALUE = CONCAT(NEW_VALUE,'FU_ID=', NEW.FU_ID,','); 
	END IF;

	IF (OLD.FUD_SIZE IS NULL AND NEW.FUD_SIZE IS NOT NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'FUD_SIZE=','<NULL>,');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'FUD_SIZE=',NEW.FUD_SIZE,',');
	ELSEIF(OLD.FUD_SIZE IS NOT NULL AND NEW.FUD_SIZE IS NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'FUD_SIZE=',OLD.FUD_SIZE,',');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'FUD_SIZE=','<NULL>,');
	ELSEIF (OLD.FUD_SIZE!= NEW.FUD_SIZE) THEN 
		SET OLD_VALUE = CONCAT(OLD_VALUE,'FUD_SIZE=', OLD.FUD_SIZE,','); 
		SET NEW_VALUE = CONCAT(NEW_VALUE,'FUD_SIZE=', NEW.FUD_SIZE,','); 
	END IF;

	IF (OLD.FUD_QUANTITY IS NULL AND NEW.FUD_QUANTITY IS NOT NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'FUD_QUANTITY=','<NULL>,');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'FUD_QUANTITY=',NEW.FUD_QUANTITY,',');
	ELSEIF(OLD.FUD_QUANTITY IS NOT NULL AND NEW.FUD_QUANTITY IS NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'FUD_QUANTITY=',OLD.FUD_QUANTITY,',');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'FUD_QUANTITY=','<NULL>,');
	ELSEIF (OLD.FUD_QUANTITY!= NEW.FUD_QUANTITY) THEN 
		SET OLD_VALUE = CONCAT(OLD_VALUE,'FUD_QUANTITY=', OLD.FUD_QUANTITY,','); 
		SET NEW_VALUE = CONCAT(NEW_VALUE,'FUD_QUANTITY=', NEW.FUD_QUANTITY,','); 
	END IF;

	IF (OLD.FUD_REMARK IS NULL AND NEW.FUD_REMARK IS NOT NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'FUD_REMARK=','<NULL>,');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'FUD_REMARK=',NEW.FUD_REMARK,',');
	ELSEIF(OLD.FUD_REMARK IS NOT NULL AND NEW.FUD_REMARK IS NULL) THEN
		SET OLD_VALUE=CONCAT(OLD_VALUE,'FUD_REMARK=',OLD.FUD_REMARK,',');
		SET NEW_VALUE=CONCAT(NEW_VALUE,'FUD_REMARK=','<NULL>,');
	ELSEIF (OLD.FUD_REMARK!= NEW.FUD_REMARK) THEN 
		SET OLD_VALUE = CONCAT(OLD_VALUE,'FUD_REMARK=', OLD.FUD_REMARK,','); 
		SET NEW_VALUE = CONCAT(NEW_VALUE,'FUD_REMARK=', NEW.FUD_REMARK,','); 
	END IF;

	IF((OLD.TRD_ID!= NEW.TRD_ID) OR (OLD.FU_ID!= NEW.FU_ID) OR
 	(OLD.FUD_SIZE IS NULL AND NEW.FUD_SIZE IS NOT NULL) OR
 	(OLD.FUD_SIZE IS NOT NULL AND NEW.FUD_SIZE IS NULL) OR
 	(OLD.FUD_SIZE!=NEW.FUD_SIZE) OR
 	(OLD.FUD_QUANTITY IS NULL AND NEW.FUD_QUANTITY IS NOT NULL) OR
 	(OLD.FUD_QUANTITY IS NOT NULL AND NEW.FUD_QUANTITY IS NULL) OR
 	(OLD.FUD_QUANTITY!=NEW.FUD_QUANTITY) OR
 	(OLD.FUD_REMARK IS NULL AND NEW.FUD_REMARK IS NOT NULL) OR
 	(OLD.FUD_REMARK IS NOT NULL AND NEW.FUD_REMARK IS NULL) OR
 	(OLD.FUD_REMARK!=NEW.FUD_REMARK)) THEN

		IF (OLD.ULD_ID!= NEW.ULD_ID) THEN  
			SET OLD_VALUE = CONCAT(OLD_VALUE,'ULD_ID=', OLD.ULD_ID,','); 
		END IF;

		IF (OLD.FUD_TIMESTAMP!= NEW.FUD_TIMESTAMP) THEN 
			SET OLD_VALUE = CONCAT(OLD_VALUE,'FUD_TIMESTAMP=', OLD.FUD_TIMESTAMP,','); 
		END IF;

	END IF;

	IF (OLD_VALUE!='' AND NEW_VALUE!='') THEN	

		IF(OLD_VALUE!=NEW_VALUE)THEN	

			SET OLD_VALUE = SUBSTRING(OLD_VALUE,1,CHAR_LENGTH(OLD_VALUE)-1);
			SET NEW_VALUE = SUBSTRING(NEW_VALUE,1,CHAR_LENGTH(NEW_VALUE)-1);			
			INSERT INTO LMC_TICKLER_HISTORY(TP_ID,ULD_ID,TTIP_ID,TH_OLD_VALUE,TH_NEW_VALUE,TH_USERSTAMP_ID)VALUES
			((SELECT TP_ID FROM LMC_TICKLER_PROFILE WHERE TP_TYPE='UPDATION'),(SELECT ULD_ID FROM LMC_TEAM_REPORT_DETAILS WHERE TRD_ID=NEW.TRD_ID),
			(SELECT TTIP_ID FROM LMC_TICKLER_TABID_PROFILE WHERE TTIP_DATA='LMC_FITTING_USAGE_DETAILS'),OLD_VALUE,NEW_VALUE,
			(SELECT ULD_ID FROM LMC_FITTING_USAGE_DETAILS WHERE FUD_ID=NEW.FUD_ID));
		
		END IF;	

	END IF;
	
END;